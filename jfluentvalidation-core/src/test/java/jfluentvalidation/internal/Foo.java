package jfluentvalidation.internal;

public class Foo {

    private int bar;
    private int baz;

    public Foo(int bar, int baz) {
        this.bar = bar;
        this.baz = baz;
    }

    public int getBar() {
        return bar;
    }

    public void setBar(int bar) {
        this.bar = bar;
    }

    public int getBaz() {
        return baz;
    }

    public void setBaz(int baz) {
        this.baz = baz;
    }
}
